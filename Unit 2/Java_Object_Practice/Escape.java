public class Escape {
    public static void main(String[] args) {
        String name1 = "shaurya"; 
        String name2 = "java"; 

        System.out.println("\"" + name1 + "\""); 
        System.out.println("\\" + name2 + "\\"); 
        System.out.println("I like to eat: \nPasta"); 
    }
    

    
}
